-- 触发操作
function action_EVENT_ANY_GADGET_DIE_20042(context, evt)
	-- 创建编号为6666（该挑战的识别id),挑战内容为184的区域挑战，具体参数填写方式，见DungeonChallengeData表中的注释，所有填写的值都必须是int类型
	if 0 ~= ScriptLib.ActiveChallenge(context, 6666, 184, 120, 4, 8888, 1) then
		return -1
	end
	
	-- 通知任务系统完成条件类型"LUA通知"，复杂参数为quest_param的进度+1
	if 0 ~= ScriptLib.AddQuestProgress(context, "4121213") then
	  return -1
	end
	
	-- 添加suite2的新内容
	    ScriptLib.AddExtraGroupSuite(context, 303001020, 2)
	
	return 0
end

-- 触发条件
function condition_EVENT_ENTER_REGION_20043(context, evt)
	if evt.param1 ~= 20043 then return false end
	
	-- 判断角色数量不少于0
	if ScriptLib.GetRegionEntityCount(context, { region_eid = evt.source_eid, entity_type = EntityType.AVATAR }) < 0 then
		return false
	end
	
	return true
end

-- 触发操作
function action_EVENT_ENTER_REGION_20043(context, evt)
	-- 终止识别id为6666的挑战，并判定成功
		ScriptLib.StopChallenge(context, 6666, 1)
	
	return 0
end

